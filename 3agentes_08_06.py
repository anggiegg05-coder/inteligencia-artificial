"""---
# AGENTE 1 — Normalizador

El Agente 1 recibe el **dataset crudo** y produce un **dataset limpio**.  
Pasos: limpieza → imputación → escalado → codificación.
"""

from sklearn.preprocessing import StandardScaler, LabelEncoder
from sklearn.impute import SimpleImputer
import re

class AgenteNormalizador:
    """
    Agente 1: Normalizador
    Responsabilidad: Recibir el CSV crudo y devolver un DataFrame limpio,
    imputado, escalado y codificado, listo para entrenamiento.
    """

    def __init__(self):
        self.scaler = StandardScaler()
        self.label_encoders = {}
        self.num_imputer = SimpleImputer(strategy='median')
        self.cat_imputer = SimpleImputer(strategy='most_frequent')
        self.col_numericas = []
        self.col_categoricas = []
        self.col_target = None

    # --- PASO 1: Limpiar ---
    def _limpiar(self, df: pd.DataFrame) -> pd.DataFrame:
        print("  [1/4]  Limpiando...")
        # Eliminar duplicados
        antes = len(df)
        df = df.drop_duplicates()
        print(f"       Duplicados eliminados: {antes - len(df)}")

        # Estandarizar nombres de columnas
        df.columns = [
            re.sub(r'[^a-zA-Z0-9_]', '_', c.strip().lower())
            for c in df.columns
        ]

        # Eliminar columnas con más del 60% de nulos
        umbral = 0.6 * len(df)
        cols_antes = df.shape[1]
        df = df.dropna(thresh=umbral, axis=1)
        print(f"       Columnas eliminadas (>60% nulos): {cols_antes - df.shape[1]}")

        # Estandarizar strings en columnas object
        for col in df.select_dtypes(include='object').columns:
            df[col] = df[col].astype(str).str.strip().str.lower()
            df[col] = df[col].replace('nan', np.nan)

        return df

    # --- PASO 2: Imputar ---
    def _imputar(self, df: pd.DataFrame) -> pd.DataFrame:
        print("  [2/4] Imputando valores nulos...")
        self.col_numericas = df.select_dtypes(include=np.number).columns.tolist()
        self.col_categoricas = df.select_dtypes(include='object').columns.tolist()

        if self.col_numericas:
            df[self.col_numericas] = self.num_imputer.fit_transform(df[self.col_numericas])

        if self.col_categoricas:
            df[self.col_categoricas] = self.cat_imputer.fit_transform(df[self.col_categoricas])

        print(f"       Nulos restantes: {df.isnull().sum().sum()}")
        return df

    # --- PASO 3: Escalar ---
    def _escalar(self, df: pd.DataFrame) -> pd.DataFrame:
        print("  [3/4]  Escalando columnas numéricas...")
        # No escalar la columna target si existe
        cols_escalar = [c for c in self.col_numericas if c != self.col_target]
        if cols_escalar:
            df[cols_escalar] = self.scaler.fit_transform(df[cols_escalar])
        return df

    # --- PASO 4: Codificar ---
    def _codificar(self, df: pd.DataFrame) -> pd.DataFrame:
        print("  [4/4]  Codificando variables categóricas...")
        for col in self.col_categoricas:
            le = LabelEncoder()
            df[col] = le.fit_transform(df[col].astype(str))
            self.label_encoders[col] = le
        return df

    # --- PIPELINE PRINCIPAL ---
    def procesar(self, df: pd.DataFrame, col_target: str = None) -> pd.DataFrame:
        print(" AGENTE 1 — NORMALIZADOR iniciado")
        print(f"   Input shape: {df.shape}")
        self.col_target = col_target

        df = df.copy()
        df = self._limpiar(df)
        df = self._imputar(df)
        df = self._escalar(df)
        df = self._codificar(df)

        print(f"   Output shape: {df.shape}")
        print(" Agente 1 finalizado\n")
        return df

