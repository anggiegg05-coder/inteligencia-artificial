"""---
#  AGENTE 2 — Entrenador

El Agente 2 recibe el **dataset limpio**, aplica validación cruzada, entrena múltiples modelos y selecciona el mejor según métricas.
"""

from sklearn.model_selection import train_test_split, cross_val_score, StratifiedKFold
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import (
    accuracy_score, f1_score, precision_score,
    recall_score, classification_report, confusion_matrix
)
from transformers import pipeline
import time

class AgenteEntrenador:
    """
    Agente 2: Entrenador
    Responsabilidad: Recibir el dataset limpio, aplicar validación cruzada,
    entrenar candidatos y seleccionar el modelo con mejor desempeño.
    Salida: modelo ganador + diccionario de métricas.
    """

    MODELOS_CANDIDATOS = {
        'RandomForest': RandomForestClassifier(n_estimators=100, random_state=42, n_jobs=-1),
        'GradientBoosting': GradientBoostingClassifier(n_estimators=100, random_state=42),
        'LogisticRegression': LogisticRegression(max_iter=500, random_state=42, n_jobs=-1)
    }

    def __init__(self, col_target: str, test_size: float = 0.2, cv_folds: int = 5):
        self.col_target = col_target
        self.test_size = test_size
        self.cv_folds = cv_folds
        self.mejor_modelo = None
        self.mejor_nombre = None
        self.metricas = {}
        self.resultados_cv = {}
        self.X_test = None
        self.y_test = None

    def _preparar_datos(self, df: pd.DataFrame):
        print("  [1/3]  Separando features y target...")
        X = df.drop(columns=[self.col_target])
        y = df[self.col_target]

        # Si hay demasiadas clases, simplificar (top N)
        top_clases = y.value_counts().nlargest(10).index
        mask = y.isin(top_clases)
        X, y = X[mask], y[mask]

        X_train, X_test, y_train, y_test = train_test_split(
            X, y, test_size=self.test_size, random_state=42, stratify=y
        )
        self.X_test = X_test
        self.y_test = y_test
        print(f"       Train: {X_train.shape} | Test: {X_test.shape} | Clases: {y.nunique()}")
        return X_train, X_test, y_train, y_test

    def _validacion_cruzada(self, X_train, y_train):
        print("  [2/3]  Validación cruzada (K-Fold = {})...".format(self.cv_folds))
        cv = StratifiedKFold(n_splits=self.cv_folds, shuffle=True, random_state=42)

        for nombre, modelo in self.MODELOS_CANDIDATOS.items():
            inicio = time.time()
            scores = cross_val_score(modelo, X_train, y_train, cv=cv,
                                     scoring='f1_weighted', n_jobs=-1)
            tiempo = time.time() - inicio
            self.resultados_cv[nombre] = {
                'mean_f1': scores.mean(),
                'std_f1': scores.std(),
                'tiempo_s': round(tiempo, 2)
            }
            print(f"       {nombre}: F1={scores.mean():.4f} ± {scores.std():.4f} ({tiempo:.1f}s)")

    def _entrenar_mejor(self, X_train, X_test, y_train, y_test):
        print("  [3/3]  Entrenando y evaluando el mejor modelo...")
        mejor_nombre = max(self.resultados_cv, key=lambda k: self.resultados_cv[k]['mean_f1'])
        mejor_modelo = self.MODELOS_CANDIDATOS[mejor_nombre]

        mejor_modelo.fit(X_train, y_train)
        y_pred = mejor_modelo.predict(X_test)

        self.mejor_modelo = mejor_modelo
        self.mejor_nombre = mejor_nombre
        self.metricas = {
            'modelo': mejor_nombre,
            'accuracy': round(accuracy_score(y_test, y_pred), 4),
            'f1_weighted': round(f1_score(y_test, y_pred, average='weighted', zero_division=0), 4),
            'precision': round(precision_score(y_test, y_pred, average='weighted', zero_division=0), 4),
            'recall': round(recall_score(y_test, y_pred, average='weighted', zero_division=0), 4),
            'cv_mean_f1': round(self.resultados_cv[mejor_nombre]['mean_f1'], 4),
            'cv_std_f1': round(self.resultados_cv[mejor_nombre]['std_f1'], 4),
            'reporte': classification_report(y_test, y_pred, zero_division=0)
        }

        print(f"       Modelo ganador: {mejor_nombre}")
        print(f"       Accuracy: {self.metricas['accuracy']} | F1: {self.metricas['f1_weighted']}")

    def entrenar(self, df: pd.DataFrame):
        print(" AGENTE 2 — ENTRENADOR iniciado")
        X_train, X_test, y_train, y_test = self._preparar_datos(df)
        self._validacion_cruzada(X_train, y_train)
        self._entrenar_mejor(X_train, X_test, y_train, y_test)
        print(" Agente 2 finalizado\n")
        return self.mejor_modelo, self.metricas

