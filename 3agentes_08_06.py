"""---
#  AGENTE 3 — Comunicador (Transformer / LLM)

El Agente 3 usa un modelo de lenguaje (Transformer) para generar un **reporte en lenguaje natural** a partir de las métricas producidas por el Agente 2.
"""

from transformers import pipeline as hf_pipeline

class AgenteComunicador:
    """
    Agente 3: Comunicador
    Responsabilidad: Recibir las métricas del modelo entrenado y generar
    un reporte en lenguaje natural usando un modelo Transformer (T5 / GPT-2).
    """

    def __init__(self, modelo_llm: str = 'google/flan-t5-base'):
        print(f"  Cargando modelo LLM: {modelo_llm}...")
        self.generador = hf_pipeline(
            'text-generation',
            model=modelo_llm,
            max_new_tokens=300
        )
        print("  Modelo cargado ")

    def _construir_prompt(self, metricas: dict, resultados_cv: dict, dataset_shape: tuple) -> str:
        cv_resumen = "\n".join(
            [f"- {k}: F1={v['mean_f1']:.4f} \u00b1 {v['std_f1']:.4f}"
             for k, v in resultados_cv.items()]
        )
        prompt = f"""You are a data scientist writing a professional report in Spanish.\nSummarize the following machine learning experiment results in clear, professional language.\n\nDataset: drug_side_effects_100k \u2014 {dataset_shape[0]} filas, {dataset_shape[1]} columnas.\nPipeline: Limpieza \u2192 Imputaci\u00f3n \u2192 Escalado \u2192 Codificaci\u00f3n \u2192 Entrenamiento con validaci\u00f3n cruzada.\n\nModelos evaluados (Cross-Validation F1 Weighted):\n{cv_resumen}\n\nModelo seleccionado: {metricas['modelo']}\nAccuracy en test: {metricas['accuracy']}\nF1 Weighted en test: {metricas['f1_weighted']}\nPrecision: {metricas['precision']}\nRecall: {metricas['recall']}\nCV F1 (media \u00b1 std): {metricas['cv_mean_f1']} \u00b1 {metricas['cv_std_f1']}\n\nWrite a professional summary report in Spanish with: introduction, methodology, results, conclusions."""
        return prompt

    def generar_reporte(self, metricas: dict, resultados_cv: dict, dataset_shape: tuple) -> str:
        print(" AGENTE 3 \u2014 COMUNICADOR iniciado")
        prompt = self._construir_prompt(metricas, resultados_cv, dataset_shape)
        print("  Generando reporte con LLM...")
        resultado = self.generador(prompt)
        reporte = resultado[0]['generated_text']
        print("\u2705 Agente 3 finalizado\n")
        return reporte

# Ejecutar Agente 3
